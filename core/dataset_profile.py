from dataclasses import dataclass, field
from typing import Dict, List


@dataclass
class DatasetProfile:
    """
    Comprehensive profile describing a training dataset.

    Generated by DatasetProfiler and consumed by the
    Model Selection & Training Agent.
    """

    # =========================================================
    # Basic Information
    # =========================================================

    task_type: str = "classification"

    target_column: str = ""

    n_samples: int = 0

    n_features: int = 0

    memory_usage_mb: float = 0.0

    duplicate_rows: int = 0

    # =========================================================
    # Column Information
    # =========================================================

    feature_names: List[str] = field(default_factory=list)

    feature_types: Dict[str, str] = field(default_factory=dict)

    numeric_columns: List[str] = field(default_factory=list)

    categorical_columns: List[str] = field(default_factory=list)

    metadata_columns: List[str] = field(default_factory=list)

    # =========================================================
    # Missing Values
    # =========================================================

    missing_counts: Dict[str, int] = field(default_factory=dict)

    missing_percentages: Dict[str, float] = field(default_factory=dict)

    overall_missing_ratio: float = 0.0

    # =========================================================
    # Cardinality
    # =========================================================

    unique_counts: Dict[str, int] = field(default_factory=dict)

    high_cardinality_columns: List[str] = field(default_factory=list)

    constant_columns: List[str] = field(default_factory=list)

    warnings: List[str] = field(default_factory=list)

    recommendations: List[str] = field(default_factory=list)

    # =========================================================
    # Target Statistics
    # =========================================================

    target_distribution: Dict = field(default_factory=dict)

    positive_samples: int = 0

    negative_samples: int = 0

    imbalance_ratio: float = 1.0

    # =========================================================
    # AutoML Characteristics
    # =========================================================

    supports_linear_models: bool = True

    supports_tree_models: bool = True

    supports_distance_models: bool = True

    supports_probabilistic_models: bool = True

    # =========================================================
    # Metadata
    # =========================================================

    metadata: Dict = field(default_factory=dict)

    # =========================================================
    # Serialization
    # =========================================================

    def to_dict(self):

        return self.__dict__
